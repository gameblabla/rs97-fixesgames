{
	SDL_Delay(sleep_time);
}

int LM_Init(unsigned char **pScreenBuffer)
{
	if(LM_GFX_Init() == 0) return 0;
	*pScreenBuffer = (unsigned char *)small_screen->pixels;
	return 1;
}

void LM_Deinit()
{
	LM_GFX_Deinit();
}

char LM_PollEvents()
{
	SDL_Event event;

	while (SDL_PollEvent(&event) != 0)
	{
		int key_scan = -1;
		unsigned char key_value = 0;

		if(event.type == SDL_QUIT) {Keys[SC_ESCAPE] = 1; return 1; }

		// unix and dingoo sdl don't have scancodes, so remap usual keys
		if(event.type == SDL_KEYDOWN) key_value = 1;
		if(event.type == SDL_KEYUP) key_value = 0;

		// Emulate x86 scancodes
		if(event.type == SDL_KEYDOWN || event.type == SDL_KEYUP)
		{
			switch(event.key.keysym.sym)
			{
				case SDLK_UP:
					key_scan = SC_UP;
					break;
				case SDLK_DOWN:
					key_scan = SC_DOWN;
					break;
				case SDLK_LEFT:
					key_scan = SC_LEFT;
					break;
				case SDLK_RIGHT:
					key_scan = SC_RIGHT;
					break;
				case SDLK_ESCAPE:
					key_scan = SC_ESCAPE;
					break;
				case SDLK_RETURN: // ENTER
					key_scan = SC_ENTER;
					break;
				case SDLK_LCTRL:
				case SDLK_LALT:
				case SDLK_LSHIFT:
				case SDLK_SPACE:
					key_scan = SC_SPACE;
					break;
				case SDLK_s:
					key_scan = SC_S;
					break;
				case SDLK_f:
					key_scan = SC_F;
					break;
				case SDLK_TAB: // LEFT SHOULDER
					key_scan = SC_TAB;
					break;
				case SDLK_BACKSPACE: // RIGHT SHOULDER
					key_scan = SC_BACKSPACE;
					break;
				default:; // maybe use in future
					break;
			}
			if(key_scan != -1) Keys[key_scan] = key_value;
		}
	}

	// toggle sizes x1 or x2 with scanlines
	// for win32 and unix only
	#ifndef __DINGUX__
	if(Keys[SC_F] == 1) {
		fullscr ^= SDL_FULLSCREEN;
		Keys[SC_F] = 0;
		LM_GFX_SetScale(2);
		return 0;
	}
	if(Keys[SC_S] == 1 && fullscr == 0) {Keys[SC_S] = 0; _toggle ^= 1; LM_GFX_SetScale(_toggle + 1);}
	#endif

	return 0;
}

int LM_GFX_Init()
{
	//Start SDL
	//if(SDL_Init(SDL_INIT_EVERYTHING) < 0) return 0;
	if(SDL_Init(SDL_INIT_VIDEO | SDL_INIT_TIMER | SDL_INIT_AUDIO) < 0) return 0;
	atexit(SDL_Quit);

#ifdef __DINGUX__
	ScreenSurface = SDL_SetVideoMode(320, 480, 16, SDL_SWSURFACE);
  screen = SDL_CreateRGBSurface(SDL_SWSURFACE, 320, 240, 16, 0, 0, 0, 0);
#else
	screen = SDL_SetVideoMode(640, 480, 8, SDL_SWSURFACE | fullscr);
	SDL_SetPalette(screen, SDL_PHYSPAL, (SDL_Color *)pal256, 0, 256);
#endif
	small_screen = SDL_CreateRGBSurface(SDL_SWSURFACE, 320, 200, 8, 0, 0, 0, 0);
	SDL_SetPalette(small_screen, SDL_LOGPAL, (SDL_Color *)pal256, 0, 256);

	SDL_ShowCursor(SDL_DISABLE);

	//Set the window caption
	SDL_WM_SetCaption("The Last Mission SDL remake", NULL);

	return 1;
}

