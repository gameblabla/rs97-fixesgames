void LM_GFX_Flip(unsigned char *p)
{
#ifdef __DINGUX__
	SDL_Rect dst;

	dst.w = 320;
	dst.h = 240;
	dst.x = 0;
	dst.y = 20;

	SDL_BlitSurface(small_screen, NULL, screen, &dst);
	//SDL_Flip(screen);

  if(SDL_MUSTLOCK(ScreenSurface)) SDL_LockSurface(ScreenSurface);
  int x, y;
  uint32_t *s = (uint32_t*)screen->pixels;
  uint32_t *d = (uint32_t*)ScreenSurface->pixels;
  for(y=0; y<240; y++){
    for(x=0; x<160; x++){
      *d++ = *s++;
    }
    d+= 160;
  }
  if(SDL_MUSTLOCK(ScreenSurface)) SDL_UnlockSurface(ScreenSurface);
  SDL_Flip(ScreenSurface);
#else
	if(scale2x == 2)
	{
		for(int y = 0; y <= 199; y++)
		{
			for(int x = 0; x <= 319; x++)
			{
				unsigned char tmp = *(unsigned char *)(p + y*320+x);
				unsigned char *pDest = (unsigned char *)screen->pixels;

				*(pDest + 640*40 + (y*2+1)*640+x*2) = tmp;
				*(pDest + 640*40 + (y*2+1)*640+x*2+1) = tmp;
				*(pDest + 640*40 + y*2*640+x*2) = tmp;
				*(pDest + 640*40 + y*2*640+x*2+1) = tmp;
			}
		}

	}
	else
	{
		memcpy(screen->pixels + 320*20, p, 320*200);
	}

	//Update Screen
	SDL_Flip(screen);
#endif
}

