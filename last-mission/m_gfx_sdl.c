void LM_ResetKeys()
{
	memset(&Keys[0], 0, 128);
}

int LM_AnyKey()
{
	for(int i = 0; i < 127; i++)
	{
		if(Keys[i] == 1) return 1;
	}
	return 0;
}

int LM_Timer()
{
	return SDL_GetTicks();
}

